			<h3>The first virtual museum to show all of Johannes Vermeer’s paintings</h3>
			<p>Today Google Arts &amp; Culture and the Mauritshuis launch Meet Vermeer, the first ever international online retrospective of Johannes Vermeer’s paintings, made in collaboration with 17 cultural partners across Europe and the United States. The highlight is the Pocket Gallery: a virtual exhibition including all of the Dutch Old Master’s works in a new Google Arts &amp; culture app that uses augmented reality. A dream for Vermeer fans worldwide.</p>
			<h4>Complete Oeuvre</h4>
			<p>On the initiative of the Mauritshuis, Google Arts &amp; Culture and 17 cultural partners joined forces 1 ½ years ago to create a platform for a broad international audience that includes the entire oeuvre of Vermeer (1632-1675) in super high resolution images and offers (art) historical information about his work and life. Unique to the project is that all of the collections that house Vermeers are participating. Even The Concert (1664), stolen from the Isabella Stewart Gardner Museum in Boston (USA) in 1990 and never recovered, is included.</p>
			<h4>Vermeer Pilgrims</h4>
			<p>They are familiar to all the museums that have Vermeers: the Vermeer pilgrims, people who travel all over the world to see all of the artist's paintings at least once in real life. They, along with many other art lovers, share one dream: the ultimate Vermeer exhibition with the entire oeuvre in one location. But this dream exhibition can never take place. We know of only 36 paintings by Johannes Vermeer. The works are in 18 collections distributed over seven countries in Europe and the United States. The condition of several paintings prohibits them from traveling; others are not permitted to leave their home museums for other reasons.</p>

